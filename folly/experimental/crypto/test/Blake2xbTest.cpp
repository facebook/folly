using namespace ::folly::crypto;

class Blake2xbTestBase : public ::testing::Test {
 protected:
  static folly::ByteRange getHashInput() {
    static const std::vector<unsigned char> bytes = []() {
      std::vector<unsigned char> b;
      b.resize(256);
      for (size_t i = 0; i < 256; ++i) {
        b[i] = static_cast<uint8_t>(i);
      }
      return b;
    }();
    return folly::range(bytes);
  }

  virtual folly::ByteRange getHashKey() const = 0;

  void checkTestVector(const std::vector<std::string>& testVector) {
    folly::ByteRange input = getHashInput();
    folly::ByteRange key = getHashKey();
    for (size_t i = 0; i < testVector.size(); ++i) {
      const auto& expectedHex = testVector[i];
      auto actualBuf = folly::IOBuf::create(expectedHex.length() / 2);
      actualBuf->append(expectedHex.length() / 2);
      Blake2xb::hash(
          {actualBuf->writableData(), actualBuf->length()}, input, key);
      std::string actualHex = folly::hexlify(actualBuf->moveToFbString());
      EXPECT_EQ(expectedHex, actualHex)
          << (key.size() == 0 ? "" : "Keyed ") << " test " << i << " failed";
    }
  }
};

